package com.dnb.pro.cart.vo;

public class CartVO {

	private String user_id;
	private String eq_name;
	private String eq_serial;
	private String wish_start;
	private String wish_end;
	
	
	
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getEq_name() {
		return eq_name;
	}
	public void setEq_name(String eq_name) {
		this.eq_name = eq_name;
	}
	public String getEq_serial() {
		return eq_serial;
	}
	public void setEq_serial(String eq_serial) {
		this.eq_serial = eq_serial;
	}
	public String getWish_start() {
		return wish_start;
	}
	public void setWish_start(String wish_start) {
		this.wish_start = wish_start;
	}
	public String getWish_end() {
		return wish_end;
	}
	public void setWish_end(String wish_end) {
		this.wish_end = wish_end;
	}
	
	
	
}
